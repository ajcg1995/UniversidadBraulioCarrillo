					importantes hábitos de estudio y se encontraban preparados para el
					ejercicio de funciones técnicas complejas. Consecuentemente,
					se dio por concluída esa etapa y se acometió la consolidacion de
					la Escuela como Insituto Parauniversitario, con autorizacíon
					oficial para otorgar los títulos de Técnicos y Diplomado de
					Aduanas. En este período, una de las tareas que se correspondío
